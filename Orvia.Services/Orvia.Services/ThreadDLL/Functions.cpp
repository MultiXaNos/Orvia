int WriteInDebugFile(std::string _line, std::string _pathDebugFile) {

    int ret;

    std::string filename(_pathDebugFile);

    std::fstream file_out;

    file_out.open(filename, std::ios_base::out);

    if (!file_out.is_open()) {

        ret = 0;

    } else {

        ret = 1;

        file_out.write(_line.c_str() + '\n', _line.size() + 1);

    }

    file_out.close();

	return ret;

}

