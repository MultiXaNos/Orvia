/////////////////////////////////////////////////////////////////////////////////////////////////////////////

int WriteInDebugFile(std::string _line, std::string _pathDebugFile) {

    int ret; 

    std::ofstream file;

    file.open(_pathDebugFile, std::ios::out | std::ios::ate | std::ios::app);

    if (file.is_open())
    {

        ret = 1;

        file << _line + '\n';

        file.close();

    } else ret = 0;

    return ret;

}

